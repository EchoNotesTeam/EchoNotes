const createJobSQL = `
INSERT INTO jobs.transcription_jobs (sheet_id, user_id, audio_path, instrument)
VALUES ($1, $2, $3, $4)
RETURNING id, sheet_id, user_id, audio_path, instrument, status, error_code, error_msg, started_at, finished_at, created_at
`

func (q *Queries) CreateJob(ctx context.Context, sheetID, userID, audioPath, instrument string) (TranscriptionJob, error) {
	row := q.db.QueryRow(ctx, createJobSQL, sheetID, userID, audioPath, instrument)
	var j TranscriptionJob
	err := row.Scan(&j.ID, &j.SheetID, &j.UserID, &j.AudioPath, &j.Instrument, &j.Status, &j.ErrorCode, &j.ErrorMsg, &j.StartedAt, &j.FinishedAt, &j.CreatedAt)
	return j, err
}

