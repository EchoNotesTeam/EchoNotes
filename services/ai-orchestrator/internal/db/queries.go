const listStalledJobsSQL = `
SELECT id, sheet_id, user_id, audio_path, instrument, status, error_code, error_msg, started_at, finished_at, created_at
FROM jobs.transcription_jobs
WHERE status IN ('queued', 'processing')
ORDER BY created_at ASC
`

func (q *Queries) ListStalledJobs(ctx context.Context) ([]TranscriptionJob, error) {
	rows, err := q.db.Query(ctx, listStalledJobsSQL)
	if err != nil {
		return nil, err
	}
	defer rows.Close()
	var jobs []TranscriptionJob
	for rows.Next() {
		var j TranscriptionJob
		if err := rows.Scan(&j.ID, &j.SheetID, &j.UserID, &j.AudioPath, &j.Instrument, &j.Status, &j.ErrorCode, &j.ErrorMsg, &j.StartedAt, &j.FinishedAt, &j.CreatedAt); err != nil {
			return nil, err
		}
		jobs = append(jobs, j)
	}
	if jobs == nil {
		jobs = []TranscriptionJob{}
	}
	return jobs, rows.Err()
}

