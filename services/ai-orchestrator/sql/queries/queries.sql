-- name: CreateJob :one
INSERT INTO jobs.transcription_jobs (sheet_id, user_id, audio_path, instrument)
VALUES ($1, $2, $3, $4)
RETURNING id, sheet_id, user_id, audio_path, instrument, status,
          error_code, error_msg, started_at, finished_at, created_at;

-- name: GetJob :one
SELECT id, sheet_id, user_id, audio_path, instrument, status,
       error_code, error_msg, started_at, finished_at, created_at
FROM jobs.transcription_jobs
WHERE id = $1;

-- name: UpdateJobProcessing :exec
UPDATE jobs.transcription_jobs
SET status = 'processing', started_at = now()
WHERE id = $1;

-- name: UpdateJobDone :exec
UPDATE jobs.transcription_jobs
SET status = 'done', finished_at = now()
WHERE id = $1;

-- name: UpdateJobFailed :exec
UPDATE jobs.transcription_jobs
SET status = 'failed', error_code = $2, error_msg = $3, finished_at = now()
WHERE id = $1;

-- name: ListStalledJobs :many
SELECT id, sheet_id, user_id, audio_path, instrument, status,
       error_code, error_msg, started_at, finished_at, created_at
FROM jobs.transcription_jobs
WHERE status IN ('queued', 'processing')
ORDER BY created_at ASC;

