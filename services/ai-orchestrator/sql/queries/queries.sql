-- UpdateSheetStatus updates the status and transcription_id of a sheet.
-- This crosses the schema boundary (sheets.sheets is TS-owned) as a deliberate
-- design choice: the Go orchestrator is the only service that knows when a
-- transcription is complete, so it updates the sheet status directly.
-- name: UpdateSheetStatus :exec
UPDATE sheets.sheets
SET status = $2, transcription_id = $3
WHERE id = $1;
